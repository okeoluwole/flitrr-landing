/**
 * Generates the playbook seed migration from the agreed content files.
 *
 *   node scripts/seed-playbooks.mjs
 *
 * Reads every content/playbooks/*.md, parses and validates it strictly
 * (lib/playbook/playbookContent.js: every field present, enums valid,
 * slugs unique; any failure throws naming the slug and field), prints the
 * per-stage counts, and writes supabase/migrations/014_playbook_seed.sql
 * as idempotent upserts keyed on slug.
 *
 * Re-run whenever the content files change, then apply 014 again in the
 * Supabase SQL editor: ON CONFLICT (slug) DO UPDATE re-seeds cleanly
 * without changing play ids, so project_playbook_state rows stay valid.
 * Plays removed from content are deliberately NOT deleted by the seed
 * (project state may reference them); retiring a play is a manual,
 * deliberate migration.
 */

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { parsePlaybookDocuments } from '../lib/playbook/playbookContent.js';

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const CONTENT_DIR = path.join(ROOT, 'content', 'playbooks');
const DEST = path.join(ROOT, 'supabase', 'migrations', '014_playbook_seed.sql');

function sqlString(value) {
  return `'${String(value).replaceAll("'", "''")}'`;
}

const files = fs
  .readdirSync(CONTENT_DIR)
  .filter((f) => f.endsWith('.md'))
  .sort();

if (files.length === 0) {
  console.error(`No content files found in ${CONTENT_DIR}.`);
  process.exit(1);
}

const documents = files.map((f) => ({
  source: f,
  content: fs.readFileSync(path.join(CONTENT_DIR, f), 'utf8'),
}));

let plays;
try {
  plays = parsePlaybookDocuments(documents);
} catch (err) {
  console.error(err.message);
  process.exit(1);
}

// The per-stage, per-type counts, for the eyeball check against the agreed
// content (Stage 2: ten action, four risk; Stage 3: nine action, four risk).
const counts = {};
for (const play of plays) {
  const key = `stage ${play.stage}`;
  counts[key] = counts[key] ?? { action: 0, risk: 0 };
  counts[key][play.type] += 1;
}
for (const [stage, c] of Object.entries(counts)) {
  console.log(`${stage}: ${c.action} action plays, ${c.risk} risk plays`);
}

const values = plays
  .map((p) =>
    [
      '  (',
      [
        sqlString(p.slug),
        sqlString(p.type) + '::play_type',
        String(p.stage),
        sqlString(p.jurisdiction),
        sqlString(p.title),
        sqlString(p.why),
        sqlString(p.objective) + '::objective_type',
        String(p.always_critical),
      ].join(', '),
      ')',
    ].join('')
  )
  .join(',\n');

const sql = `-- ========================================================================
-- 014_playbook_seed.sql
--
-- GENERATED by scripts/seed-playbooks.mjs from content/playbooks/*.md
-- (${files.join(', ')}). Do not edit by hand: edit the content files and
-- re-run the script, then apply this again in the Supabase SQL editor.
--
-- Idempotent: upserts keyed on slug, so a re-run updates the curated
-- fields in place without changing any play's id (project_playbook_state
-- rows keep pointing at the same plays). Plays removed from content are
-- not deleted here; retiring a play is a deliberate manual migration.
--
-- Run AFTER 013_playbook_engine.sql.
-- ========================================================================

INSERT INTO playbook_plays
  (slug, type, stage, jurisdiction, title, why, objective, always_critical)
VALUES
${values}
ON CONFLICT (slug) DO UPDATE SET
  type            = EXCLUDED.type,
  stage           = EXCLUDED.stage,
  jurisdiction    = EXCLUDED.jurisdiction,
  title           = EXCLUDED.title,
  why             = EXCLUDED.why,
  objective       = EXCLUDED.objective,
  always_critical = EXCLUDED.always_critical;
`;

fs.writeFileSync(DEST, sql, 'utf8');
console.log(`${plays.length} plays written to ${path.relative(ROOT, DEST)}`);
